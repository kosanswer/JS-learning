/*
В JavaScript, метод `.join()` применяется к массиву для объединения всех его элементов в строку. 
Метод возвращает новую строку, в которой элементы массива объединены указанным разделителем.

Пример использования метода `.join()`:

```javascript
const myArray = ['Hello', 'World', '!'];
const joinedString = myArray.join(' ');
console.log(joinedString);
```

Вывод:
```
Hello World !
```

В примере выше, метод `.join(' ')` объединяет элементы массива `myArray` в строку, используя пробел в качестве разделителя.

Если вызвать метод `.join()` без аргументов, то элементы массива будут объединены в строку без каких-либо разделителей:

```javascript
const myArray = ['apple', 'banana', 'orange'];
const joinedString = myArray.join();
console.log(joinedString);
```

Вывод:
```
apple,banana,orange
```

В этом примере элементы массива объединяются в строку с использованием запятых в качестве разделителей.

Обратите внимание, что метод `.join()` не изменяет исходный массив, а возвращает новую строку.
*/
