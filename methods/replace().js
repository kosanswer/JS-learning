/*
В JavaScript метод `replace()` применяется к строке и позволяет заменить часть строки на другую строку или регулярное выражение.

Синтаксис метода `replace()` выглядит следующим образом:

```javascript
str.replace(searchValue, newValue)
```

- `str`: Исходная строка, в которой нужно выполнить замену.
- `searchValue`: Строка или регулярное выражение, которое нужно найти и заменить в исходной строке.
- `newValue`: Новая строка, которой нужно заменить найденное значение.

Вот пример использования метода `replace()`:

```javascript
const str = "Hello, World!";
const newStr = str.replace("Hello", "Hi");
console.log(newStr);  // Вывод: "Hi, World!"
```

В этом примере метод `replace()` применяется к строке `"Hello, World!"`.
Он ищет подстроку `"Hello"` в исходной строке и заменяет ее на новую строку `"Hi"`. Результатом замены будет новая строка `"Hi, World!"`.

Метод `replace()` может также использовать регулярные выражения для поиска и замены более сложных паттернов в строке. Например:

```javascript
const str = "The quick brown fox jumps over the lazy dog";
const newStr = str.replace(/o/g, "x");
console.log(newStr);  // Вывод: "The quick brxwn fxx jumps xver the lazy dxg"
```

В этом примере регулярное выражение `/o/g` ищет все вхождения символа `"o"` в строке и заменяет их на символ `"x"`. 
Результатом замены будет новая строка, в которой все `"o"` заменены на `"x"`.

Обратите внимание, что метод `replace()` не изменяет исходную строку, а возвращает новую строку с выполненными заменами.
*/
